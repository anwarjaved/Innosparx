namespace Framework.FileSystem
{
    ///-------------------------------------------------------------------------------------------------
    /// <summary>
    ///     Interface for virtual file item.
    /// </summary>
    ///
    /// <remarks>
    ///     Anwar Javed, 09/13/2013 4:54 PM.
    /// </remarks>
    ///-------------------------------------------------------------------------------------------------
    public interface IVirtualFileItem
    {
        ///-------------------------------------------------------------------------------------------------
        /// <summary>
        ///     Gets the name.
        /// </summary>
        ///
        /// <value>
        ///     The name.
        /// </value>
        ///-------------------------------------------------------------------------------------------------
        string Name { get; }

        ///-------------------------------------------------------------------------------------------------
        /// <summary>
        ///     Gets the full pathname of the relative file.
        /// </summary>
        ///
        /// <value>
        ///     The full pathname of the relative file.
        /// </value>
        ///-------------------------------------------------------------------------------------------------
        string RelativePath { get; }

        ///-------------------------------------------------------------------------------------------------
        /// <summary>
        ///     Gets the full pathname of the absolute file.
        /// </summary>
        ///
        /// <value>
        ///     The full pathname of the absolute file.
        /// </value>
        ///-------------------------------------------------------------------------------------------------
        string AbsolutePath { get; }

        ///-------------------------------------------------------------------------------------------------
        /// <summary>
        ///     Gets Web URL for item.
        /// </summary>
        ///
        /// <value>
        ///     The web URL.
        /// </value>
        ///-------------------------------------------------------------------------------------------------
        string WebUrl { get; }

        ///-------------------------------------------------------------------------------------------------
        /// <summary>
        ///     Gets a value indicating whether this VirtualFileItem is folder.
        /// </summary>
        ///
        /// <value>
        ///     true if this VirtualFileItem is folder, false if not.
        /// </value>
        ///-------------------------------------------------------------------------------------------------
        bool IsFolder { get; }

        ///-------------------------------------------------------------------------------------------------
        /// <summary>
        ///     Gets the file system.
        /// </summary>
        ///
        /// <value>
        ///     The file system.
        /// </value>
        ///-------------------------------------------------------------------------------------------------
        IVirtualFileSystem FileSystem { get; }
    }
}